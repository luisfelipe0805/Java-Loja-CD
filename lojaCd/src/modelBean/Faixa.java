package modelBean;

public class Faixa {
	private int idMusica;
	private int idNumeroFaixa;
	private int idCd;
	public int getIdMusica() {
		return idMusica;
	}
	public void setIdMusica(int idMusica) {
		this.idMusica = idMusica;
	}
	public int getIdNumeroFaixa() {
		return idNumeroFaixa;
	}
	public void setIdNumeroFaixa(int idNumeroMusica) {
		this.idNumeroFaixa = idNumeroMusica;
	}
	public int getIdCd() {
		return idCd;
	}
	public void setIdCd(int idCd) {
		this.idCd = idCd;
	}
	
}
