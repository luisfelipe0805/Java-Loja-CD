package modelBean;

public class Gravadora {
	private int idGravador;
	private String nomeGravadora;
	private String endereco;
	private String telefone;
	private String contato;
	private String url;
	public int getIdGravador() {
		return idGravador;
	}
	
	
	public void setIdGravador(int idGravador) {
		this.idGravador = idGravador;
	}
	public String getNomeGravadora() {
		return nomeGravadora;
	}
	public void setNomeGravadora(String nomeGravadora) {
		this.nomeGravadora = nomeGravadora;
	}
	public String getEndereco() {
		return endereco;
	}
	public void setEndereco(String endereco) {
		this.endereco = endereco;
	}
	public String getTelefone() {
		return telefone;
	}
	public void setTelefone(String telefone) {
		this.telefone = telefone;
	}
	public String getContato() {
		return contato;
	}
	public void setContato(String contato) {
		this.contato = contato;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	
	
}
